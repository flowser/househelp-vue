    {
                $share = new Share();
                $share->user_id            = Auth::user()->id;
                $share->referral_link_id   = $request->referral_link_id;
                $share->network            = $request->network;
                $share->save();

                return response()-> json([
                    'code'      =>  200,
                ], 200);
    }

